#include "lem_in.h"

void		index_initialiser(t_index **i)
{
	static int		c = 0;

	c++;
	if (c == 1)
		(*i) = (t_index *)ft_memalloc(sizeof(t_index));
	if ((*i))
	{
		(*i)->i = 0;
		(*i)->j = 0;
		(*i)->k = 0;
		(*i)->l = 0;
		(*i)->m = 0;
	}
}
